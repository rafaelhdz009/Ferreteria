    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            comoBoxRFC.removeAllItems();
            this.comoBoxRFC.addItem("Selecciona el RFC");
            List<String> listRFC = this.ferrD.listaClienteLikeRFC(this.txtRFC.getText().toUpperCase());
            for (String rfc : listRFC) {
                this.comoBoxRFC.addItem(rfc);
            }
            this.comoBoxRFC.setVisible(true);
        } catch (Exception e) {
            this.comoBoxRFC.addItem("Selecciona el RFC");
            List<String> listRFC = this.ferrD.listaClienteLikeRFC(this.txtRFC.getText().toUpperCase());
            for (String rfc : listRFC) {
                this.comoBoxRFC.addItem(rfc);
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void txtRFCKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRFCKeyPressed
        String s = "";
        if (evt.getKeyChar() == '\n') {
            Validaciones.enter(this, evt, this.btnBuscar);
            CtrlInterfaz.habilita(true, btnBuscar);
        }
    }//GEN-LAST:event_txtRFCKeyPressed

    private void txtRFCKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRFCKeyTyped
        Validaciones.validaAlfanumerico(evt, 13, txtRFC.getText());
    }//GEN-LAST:event_txtRFCKeyTyped

    private void btnBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btnBuscarKeyPressed
        if (evt.getKeyChar() == '\n') {
            btnBuscarActionPerformed(null);
        }
    }//GEN-LAST:event_btnBuscarKeyPressed

    private void comoBoxRFCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comoBoxRFCActionPerformed
        if (this.comoBoxRFC.getSelectedIndex() != 0) {
            String rfc = String.valueOf(this.comoBoxRFC.getSelectedItem());
            Cliente c = this.ferrD.listaClienteWhereRFC(rfc);
            String desp = c.toString();
            this.txtConsultaClv.setText(desp);
        } else {
            this.txtConsultaClv.setText("");
        }
    }//GEN-LAST:event_comoBoxRFCActionPerformed

    public void llenaComboCve() {
        List<String> arr = ferrD.listaRFCliente();
        for (String rfc : arr) {
            this.comoBoxRFC.addItem(rfc);
        }
    }

